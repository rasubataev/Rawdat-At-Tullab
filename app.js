  $('#k-streak').textContent = STATE.streak.current;
  $('#k-streak-d').textContent = STATE.streak.current >= 1 ? `Лучшая: ${STATE.streak.best}` : 'Начни сегодня';
  $('#k-today').textContent = today.reviews;
