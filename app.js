function renderLesson() {
  const lesson = LESSONS[LESSON_ID];
  if (!lesson) {
    $('#lesson-body').innerHTML = `<div class="empty-state"><div class="es-title">Скоро</div><div class="es-text">Урок в разработке</div></div>`;
    $('#lesson-title').textContent = 'Урок';
    return;
  }
  $('#lesson-title').textContent = lesson.title;
  let html = '';

  // Арабский текст
  html += `<div class="lesson-arabic-text">${esc(lesson.arabicText).replace(/\n/g,'<br>')}</div>`;

  // Объяснение
  html += `<div class="lesson-explain">
    <div class="lesson-explain-title">
      <span style="background:var(--brand-100);color:var(--brand-600);width:22px;height:22px;border-radius:50%;display:grid;place-items:center;font-size:13px;flex-shrink:0">?</span>
      Что такое речь (الكَلام)?
    </div>
    <div class="lesson-explain-text">${esc(lesson.explain)}</div>
  </div>`;

  // Карточки 1/2/3
  html += `<div class="lesson-cards-row">`;
  lesson.cards.forEach(c => {
    html += `<div class="lesson-card">
      <div class="lesson-card-num" style="background:${c.color}">${c.num}</div>
      <div class="lesson-card-ar">${esc(c.ar)}</div>
      <div class="lesson-card-label">${esc(c.label).replace(/\n/g,'<br>')}</div>
    </div>`;
  });
  html += `</div>`;

  // Секции
  lesson.sections.forEach(s => {
    html += `<div class="lesson-section">
      <div class="lesson-section-title" style="color:${s.color}">${esc(s.title)}</div>`;
    s.bullets.forEach(b => {
      html += `<div class="lesson-bullet">${esc(b)}</div>`;
    });
    if (s.example) {
      html += `<div class="lesson-example">
        <div class="ex-ar" style="color:${s.color}">${esc(s.example.ar)}</div>
        <div style="font-size:13px;color:var(--text-3);margin-top:4px">${esc(s.example.ru)}</div>
      </div>`;
    }
    html += `</div>`;
  });

  // Совет
  if (lesson.tip) {
    html += `<div class="lesson-tip">
      <div class="lesson-tip-icon">💡</div>
      <div class="lesson-tip-text"><b>Важно запомнить:</b><br>${esc(lesson.tip)}</div>
    </div>`;
  }

  $('#lesson-body').innerHTML = html;
}
